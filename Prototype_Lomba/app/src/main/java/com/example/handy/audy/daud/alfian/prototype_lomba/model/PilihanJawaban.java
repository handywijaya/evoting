public class PilihanJawaban {
    private String idPilihan;
    private String idSoal;
    private String namaPilihan;

    public String getIdPilihan() {
        return idPilihan;
    }

    public void setIdPilihan(String idPilihan) {
        this.idPilihan = idPilihan;
    }

    public String getIdSoal() {
        return idSoal;
    }

    public void setIdSoal(String idSoal) {
        this.idSoal = idSoal;
    }

    public String getNamaPilihan() {
        return namaPilihan;
    }

    public void setNamaPilihan(String namaPilihan) {
        this.namaPilihan = namaPilihan;
    }

}
